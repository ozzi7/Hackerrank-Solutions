// Complete the workbook function below.
int workbook(int n, int k, vector<int> arr) {
	int currPage = 0;
	int res = 0;
	for (int i = 0; i < n; ++i) {
		for (int problem = 1; problem <= arr[i]; ++problem)
		{
			if ((problem -1) % k == 0) currPage++; // this problem is on new page
			if (problem == currPage) res++;
		}
	}
	return res;
}

